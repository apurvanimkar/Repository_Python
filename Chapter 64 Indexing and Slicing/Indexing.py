#Indexing and Slicing

#Example of Indexing in strings
s="Python"
print (s[0])#P
print (s[-1])#n
print (s[-2])#o

#Example of Indexing in List
list_=[10,20,30,40,50]
print (list_[0])#10
print (list_[-1])#50
print (list_[-2])#40

#Indexing in tuple 
t=(1,2,3,4,5)
print (t[0])
print (t[-1])
print (t[-2])

#Indexing - range object
r=range(5)
print (r[0])#0
print (r[-1])#4
print (r[-2])#3

