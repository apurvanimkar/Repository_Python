#Rounding: round, floor, ceil, trunc

import math

x=1.55
print(math.floor(x))

print(math.ceil(x))

print(math.trunc(x))

print(round(1.33, 1))

#Trigonometry
print(math.sin(math.pi / 2))#1.0

print(math.asin(1))

print(math.asin(1) / math.pi)

print(math.cos(math.pi / 2))

print(math.acos(1))

print(math.tan(math.pi/2))
#Pow for faster exponentiation
print(pow(5,5))

#Logarithms
print(math.log(math.e)) # 1.0
print(math.log(1)) # 0.0
print(math.log(100)) # 4.605170185988092

#Constants
from math import pi, e
print(pi)
print(e)
